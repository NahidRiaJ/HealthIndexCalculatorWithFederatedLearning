    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        // TODO add your handling code here:
        double avg = sum / n; 
        jTextField2.setText(String.format("%.2f", avg)); 

        homeFrame.setAvgBloodSugar(avg);
        if (avg < 4.0) {
            jTextArea1.setText("Warning: Your blood sugar level is below the normal range. Please contact your doctor.");
        } else if (avg > 7.8) {
            jTextArea1.setText("Warning: Your blood sugar level is above the normal range. Please contact your doctor.");
        } else {
            jTextArea1.setText("Your blood sugar level is within the normal range.");
        }
        homeFrame.insertBloodSuger(id,name, avg);
    count1=0;count2=0;
    sum=0;
    n=0;
    homeFrame.setAvgBloodSugar(avg);
    homeFrame.setId(id);
        homeFrame.setName(name);
        homeFrame.saveToDatabase();
        
    }//GEN-LAST:event_button2ActionPerformed

