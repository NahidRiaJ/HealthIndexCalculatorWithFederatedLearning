    private void button6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button6ActionPerformed
        // TODO add your handling code here:
        //implementation 
        try {
    String query = "SELECT recorded_at, avg_blood_sugar FROM blood_sugar_data WHERE id = " + id;

    // Create a dataset
    JDBCCategoryDataset dataset = new JDBCCategoryDataset(DatabaseConnection.ConnectDb(), query);

    // Create a line chart using the dataset
    JFreeChart chart = ChartFactory.createLineChart(
            "Blood Sugar Over Time (ID: " + id + ")", // Chart title
            "Date",                                   // X-axis Label
            "Avg Blood Sugar",                        // Y-axis Label
            dataset,                                  // Dataset
            PlotOrientation.VERTICAL,                 // Chart Orientation
            true,                                     // Include Legend
            true,                                     // Use Tooltips
            false                                     // URLs
    );

    // Customize chart
    chart.setBackgroundPaint(java.awt.Color.white);

    // Renderer for lines and dots
    CategoryPlot plot = chart.getCategoryPlot();
    LineAndShapeRenderer renderer = new LineAndShapeRenderer();
    renderer.setSeriesLinesVisible(0, true);  // Enable lines
    renderer.setSeriesShapesVisible(0, true); // Enable dots
    plot.setRenderer(renderer);

    // Display the chart
    ChartPanel chartPanel = new ChartPanel(chart);
    chartPanel.setPreferredSize(new java.awt.Dimension(800, 600));

    JFrame chartFrame = new JFrame("Blood Sugar Chart");
    chartFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
    chartFrame.setSize(900, 700);
    chartFrame.add(chartPanel);
    chartFrame.setVisible(true);

} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(
            null,
            "Error: " + e.getMessage(),
            "Query Chart Error",
            JOptionPane.ERROR_MESSAGE
    );
}

    }//GEN-LAST:event_button6ActionPerformed
    
